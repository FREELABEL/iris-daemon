#!/usr/bin/env node
/**
 * Hive Network Test — Multi-Node A2A Integration Test
 *
 * Manages the full lifecycle:
 *   1. Register two compute nodes with iris-api
 *   2. Write API keys to .env.hive-test
 *   3. Start Docker containers (node-alpha, node-beta)
 *   4. Wait for both nodes to come online
 *   5. Dispatch independent tasks to each node
 *   6. Dispatch a chained task (alpha → beta pipeline)
 *   7. Report results with timing
 *
 * Usage:
 *   node test-hive-network.js                     # Full test (setup + dispatch + teardown)
 *   node test-hive-network.js --setup-only        # Just register nodes + write .env
 *   node test-hive-network.js --dispatch-only     # Skip setup, just run tests
 *   node test-hive-network.js --no-teardown       # Leave containers running after test
 *   node test-hive-network.js --api-url <url>     # Override iris-api URL
 *
 * Prerequisites:
 *   - iris-api running locally (Docker or native)
 *   - Docker available for container management
 */

const http = require('http')
const https = require('https')
const { URL } = require('url')
const { execSync, exec } = require('child_process')
const fs = require('fs')
const path = require('path')

// ─── Config ────────────────────────────────────────────────────────
const args = process.argv.slice(2)
const config = {
  apiUrl: getArg('--api-url') || process.env.IRIS_API_URL || 'https://local.iris.freelabel.net',
  userId: getArg('--user-id') || '1',
  setupOnly: args.includes('--setup-only'),
  dispatchOnly: args.includes('--dispatch-only'),
  noTeardown: args.includes('--no-teardown'),
  verbose: args.includes('--verbose') || args.includes('-v'),
  dockerDir: path.resolve(__dirname, '..')
}

const envFile = path.join(config.dockerDir, '.env.hive-test')

const PASS = '\x1b[32m\u2714\x1b[0m'
const FAIL = '\x1b[31m\u2718\x1b[0m'
const INFO = '\x1b[36m\u2192\x1b[0m'
const BOLD = '\x1b[1m'
const DIM = '\x1b[2m'
const RESET = '\x1b[0m'
const GREEN = '\x1b[32m'
const YELLOW = '\x1b[33m'
const CYAN = '\x1b[36m'

// ─── Main ──────────────────────────────────────────────────────────
async function main () {
  printBanner()

  const results = { passed: 0, failed: 0, skipped: 0 }
  const start = Date.now()

  if (!config.dispatchOnly) {
    // Phase 1: Register nodes with iris-api
    await phase('PHASE 1: Node Registration', async () => {
      const alphaKey = await registerNode(results, 'Node Alpha', 'hive-test-alpha')
      const betaKey = await registerNode(results, 'Node Beta', 'hive-test-beta')

      if (!alphaKey || !betaKey) {
        throw new Error('Failed to register both nodes')
      }

      // Write .env.hive-test
      const envContent = [
        '# Auto-generated by test-hive-network.js',
        `# ${new Date().toISOString()}`,
        `HIVE_NODE_ALPHA_KEY=${alphaKey}`,
        `HIVE_NODE_BETA_KEY=${betaKey}`
      ].join('\n') + '\n'

      fs.writeFileSync(envFile, envContent)
      console.log(`  ${PASS} Wrote API keys to .env.hive-test`)
      results.passed++
    })

    if (config.setupOnly) {
      printNetworkTable()
      printSummary(results, Date.now() - start)
      console.log(`  ${INFO} Run ${BOLD}npm run hive:test:up${RESET} to start the containers`)
      console.log(`  ${INFO} Then ${BOLD}npm run hive:test:dispatch${RESET} to run tasks\n`)
      return
    }

    // Phase 2: Start Docker containers
    await phase('PHASE 2: Container Startup', async () => {
      await runTest(results, 'Build hive-daemon image', async () => {
        shell(`cd "${config.dockerDir}" && docker compose --profile hive-test build hive-node-alpha 2>&1 | tail -3`)
        return 'Image built'
      })

      await runTest(results, 'Start node-alpha + node-beta containers', async () => {
        shell(`cd "${config.dockerDir}" && docker compose --env-file .env.hive-test --profile hive-test up -d hive-node-alpha hive-node-beta 2>&1`)
        return 'Containers started'
      })
    })

    // Phase 3: Wait for nodes to come online
    await phase('PHASE 3: Node Health Check', async () => {
      await runTest(results, 'Node Alpha comes online', async () => {
        return await waitForNode('hive-test-alpha', 30000)
      })

      await runTest(results, 'Node Beta comes online', async () => {
        return await waitForNode('hive-test-beta', 30000)
      })
    })
  }

  // Phase 4: Dispatch tasks
  await phase('PHASE 4: Task Dispatch', async () => {
    // Get online nodes
    const nodes = await getOnlineNodes()
    const alpha = nodes.find(n => n.name === 'hive-test-alpha')
    const beta = nodes.find(n => n.name === 'hive-test-beta')

    if (!alpha || !beta) {
      const online = nodes.map(n => n.name).join(', ') || 'none'
      throw new Error(`Need both alpha + beta online. Currently online: ${online}`)
    }

    // Test A: Independent tasks to each node
    await runTest(results, 'Dispatch echo task to Node Alpha', async () => {
      const taskId = await createTask({
        title: '[HIVE-TEST] Alpha Echo',
        type: 'custom',
        prompt: 'echo "ALPHA_OK: $(hostname) $(date +%s)" && sleep 1 && echo "alpha done"',
        node_id: alpha.id,
        config: { timeout_seconds: 30 }
      })
      return `Task ${taskId.substring(0, 8)} dispatched`
    })

    await runTest(results, 'Dispatch echo task to Node Beta', async () => {
      const taskId = await createTask({
        title: '[HIVE-TEST] Beta Echo',
        type: 'custom',
        prompt: 'echo "BETA_OK: $(hostname) $(date +%s)" && sleep 1 && echo "beta done"',
        node_id: beta.id,
        config: { timeout_seconds: 30 }
      })
      return `Task ${taskId.substring(0, 8)} dispatched`
    })

    // Wait for both to complete
    await runTest(results, 'Both tasks complete independently', async () => {
      const tasks = await getRecentTasks()
      const testTasks = tasks.filter(t => t.title && t.title.includes('[HIVE-TEST]'))
      const pending = testTasks.filter(t => !['completed', 'failed'].includes(t.status))

      if (pending.length > 0) {
        // Poll for completion
        const deadline = Date.now() + 30000
        while (Date.now() < deadline) {
          const updated = await getRecentTasks()
          const still = updated.filter(t =>
            t.title && t.title.includes('[HIVE-TEST]') &&
            !['completed', 'failed'].includes(t.status)
          )
          if (still.length === 0) break
          await sleep(2000)
        }
      }

      const final = await getRecentTasks()
      const alphaTask = final.find(t => t.title && t.title.includes('Alpha Echo'))
      const betaTask = final.find(t => t.title && t.title.includes('Beta Echo'))

      const alphaOk = alphaTask?.status === 'completed'
      const betaOk = betaTask?.status === 'completed'

      if (!alphaOk || !betaOk) {
        throw new Error(`Alpha: ${alphaTask?.status || 'missing'}, Beta: ${betaTask?.status || 'missing'}`)
      }

      return `Alpha: completed, Beta: completed`
    })

    // Test B: Demonstrate A2A potential — alpha writes data, beta reads it
    await runTest(results, 'A2A data handoff (alpha writes, beta reads)', async () => {
      // Alpha writes a "lead list" to shared volume
      const writeTaskId = await createTask({
        title: '[HIVE-TEST] A2A Write',
        type: 'custom',
        prompt: 'mkdir -p /data/project/hive-shared && echo \'{"leads":[{"name":"alice","score":92},{"name":"bob","score":87}],"source":"node-alpha","ts":"\'$(date +%s)\'"}\' > /data/project/hive-shared/a2a-handoff.json && echo "A2A_WRITE_OK"',
        node_id: alpha.id,
        config: { timeout_seconds: 15 }
      })

      // Wait for alpha to finish writing
      await waitForTask(writeTaskId, 15000)

      // Beta reads the data alpha wrote
      const readTaskId = await createTask({
        title: '[HIVE-TEST] A2A Read',
        type: 'custom',
        prompt: 'cat /data/project/hive-shared/a2a-handoff.json && echo "A2A_READ_OK"',
        node_id: beta.id,
        config: { timeout_seconds: 15 }
      })

      const readResult = await waitForTask(readTaskId, 15000)
      const output = readResult?.result?.output || ''

      if (!output.includes('A2A_READ_OK') || !output.includes('node-alpha')) {
        throw new Error(`Beta couldn't read Alpha's data: ${output.substring(0, 100)}`)
      }

      return 'Alpha wrote leads.json, Beta read it successfully'
    })
  })

  // Phase 5: Network topology report
  printNetworkTable()
  printA2AArchitecture()

  // Teardown
  if (!config.noTeardown && !config.dispatchOnly) {
    console.log(`\n  ${DIM}Tearing down test containers...${RESET}`)
    try {
      shell(`cd "${config.dockerDir}" && docker compose --profile hive-test stop hive-node-alpha hive-node-beta 2>&1`)
      // Clean up shared handoff file
      const handoff = path.join(config.dockerDir, 'hive-shared', 'a2a-handoff.json')
      if (fs.existsSync(handoff)) fs.unlinkSync(handoff)
      const handoffDir = path.join(config.dockerDir, 'hive-shared')
      if (fs.existsSync(handoffDir)) fs.rmdirSync(handoffDir)
    } catch { /* best effort */ }
  }

  printSummary(results, Date.now() - start)
  process.exit(results.failed > 0 ? 1 : 0)
}

// ─── Node Registration ────────────────────────────────────────────
async function registerNode (results, displayName, nodeName) {
  let apiKey = null
  await runTest(results, `Register ${displayName}`, async () => {
    // Check if node already exists
    const existing = await apiGet('/api/v6/nodes', { user_id: config.userId })
    const nodes = existing.nodes || []
    const found = nodes.find(n => n.name === nodeName)

    if (found) {
      // Delete and re-register for clean test
      try {
        await apiDelete(`/api/v6/nodes/${found.id}`, { user_id: config.userId })
      } catch { /* might fail, continue */ }
    }

    // Register new node
    const res = await apiPost('/api/v6/nodes', {
      name: nodeName,
      user_id: config.userId,
      capabilities: { docker: true, a2a: true }
    })

    if (!res.node || !res.node.api_key) {
      throw new Error(`Registration failed: ${JSON.stringify(res).substring(0, 200)}`)
    }

    apiKey = res.node.api_key
    return `ID: ${res.node.id.substring(0, 8)}... | Key: ${apiKey.substring(0, 16)}...`
  })
  return apiKey
}

// ─── Helpers ───────────────────────────────────────────────────────
async function getOnlineNodes () {
  const res = await apiGet('/api/v6/nodes', { user_id: config.userId })
  return (res.nodes || []).filter(n => n.connection_status === 'online')
}

async function getRecentTasks () {
  const res = await apiGet('/api/v6/nodes/tasks', { user_id: config.userId })
  return res.tasks || []
}

async function createTask (payload) {
  payload.user_id = config.userId
  const res = await apiPost('/api/v6/nodes/tasks', payload)
  if (!res.task || !res.task.id) throw new Error(`Task creation failed: ${JSON.stringify(res).substring(0, 200)}`)
  return res.task.id
}

async function waitForNode (nodeName, timeout) {
  const deadline = Date.now() + timeout
  while (Date.now() < deadline) {
    const nodes = await getOnlineNodes()
    const found = nodes.find(n => n.name === nodeName)
    if (found) return `Online (ID: ${found.id.substring(0, 8)}...)`
    await sleep(3000)
  }
  throw new Error(`${nodeName} did not come online within ${timeout / 1000}s`)
}

async function waitForTask (taskId, timeout) {
  const deadline = Date.now() + timeout
  while (Date.now() < deadline) {
    const res = await apiGet(`/api/v6/nodes/tasks/${taskId}`, { user_id: config.userId })
    const task = res.task || res
    if (task.status === 'completed') return task
    if (task.status === 'failed') throw new Error(`Task failed: ${task.result?.error || 'unknown'}`)
    await sleep(2000)
  }
  throw new Error(`Task ${taskId} timed out after ${timeout / 1000}s`)
}

async function waitForBoth (taskIds, timeout) {
  const deadline = Date.now() + timeout
  while (Date.now() < deadline) {
    let allDone = true
    for (const id of taskIds) {
      const res = await apiGet(`/api/v6/nodes/tasks/${id}`, { user_id: config.userId })
      const task = res.task || res
      if (!['completed', 'failed'].includes(task.status)) allDone = false
    }
    if (allDone) return
    await sleep(2000)
  }
  throw new Error(`Tasks did not complete within ${timeout / 1000}s`)
}

