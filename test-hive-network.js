async function phase (title, fn) {
  console.log(`\n  ${BOLD}${title}${RESET}`)
  console.log(`  ${'─'.repeat(title.length)}`)
  try {
    await fn()
  } catch (err) {
    console.log(`  ${FAIL} Phase failed: ${err.message}`)
  }
}

async function runTest (results, name, fn) {
  try {
    const detail = await fn()
    console.log(`  ${PASS} ${name}`)
    if (detail) console.log(`    ${DIM}${detail}${RESET}`)
    results.passed++
  } catch (err) {
    console.log(`  ${FAIL} ${name}`)
    console.log(`    \x1b[31m${err.message}\x1b[0m`)
    results.failed++
  }
}

function printSummary (results, elapsed) {
  console.log('')
  console.log('  ══════════════════════════════════════')
  const status = results.failed === 0 ? `${GREEN}ALL PASSED${RESET}` : `\x1b[31mFAILED\x1b[0m`
  console.log(`  ${status} — ${results.passed} passed, ${results.failed} failed (${(elapsed / 1000).toFixed(1)}s)`)
  console.log('')
}

function shell (cmd) {
  return execSync(cmd, { encoding: 'utf-8', timeout: 120000 }).trim()
}

// ─── HTTP ──────────────────────────────────────────────────────────
function apiGet (urlPath, params = {}) {
  const qs = new URLSearchParams(params).toString()
  const fullPath = qs ? `${urlPath}?${qs}` : urlPath
  return request('GET', fullPath)
}

function apiPost (urlPath, body) {
  return request('POST', urlPath, body)
}

function apiDelete (urlPath, params = {}) {
  const qs = new URLSearchParams(params).toString()
  const fullPath = qs ? `${urlPath}?${qs}` : urlPath
  return request('DELETE', fullPath)
}

function request (method, urlPath, body = null) {
  return new Promise((resolve, reject) => {
    const url = new URL(urlPath, config.apiUrl)
    const isHttps = url.protocol === 'https:'
    const lib = isHttps ? https : http

    const options = {
      hostname: url.hostname,
      port: url.port || (isHttps ? 443 : 80),
      path: url.pathname + url.search,
      method,
      headers: {
        Accept: 'application/json',
        'Content-Type': 'application/json',
        'User-Agent': 'Hive-Network-Test/1.0'
      },
      rejectUnauthorized: false
    }

    const req = lib.request(options, (res) => {
      let data = ''
      res.on('data', (chunk) => { data += chunk })
      res.on('end', () => {
        if (res.statusCode >= 200 && res.statusCode < 300) {
          try { resolve(JSON.parse(data)) } catch { resolve(data) }
        } else {
          reject(new Error(`HTTP ${res.statusCode}: ${data.substring(0, 300)}`))
        }
      })
    })

    req.on('error', reject)
    req.setTimeout(15000, () => { req.destroy(); reject(new Error('Request timeout')) })
    if (body) req.write(JSON.stringify(body))
    req.end()
  })
}

function getArg (name) {
  const idx = args.indexOf(name)
  return idx !== -1 && args[idx + 1] ? args[idx + 1] : null
}

function sleep (ms) {
  return new Promise(resolve => setTimeout(resolve, ms))
}

main().catch(err => {
  console.error(`\n  ${FAIL} Unexpected error: ${err.message}`)
  if (config.verbose) console.error(err.stack)
  process.exit(1)
})
